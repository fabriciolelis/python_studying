import math

angulo = int(input('Digite o ângulo que você deseja: '))
print('O valor do cosseno do ângulo {} é {:.2f}'.format(angulo, math.cos(angulo)))
print('O valor do seno do ângulo {} é {:.2f}'.format(angulo, math.sin(angulo)))
print('O valor da tagente do ângulo {} é {:.2f}'.format(angulo, math.tan(angulo)))
