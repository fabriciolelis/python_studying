primeiraNotaStr = input("Digite a primeira nota: ")
primeiraNota = int(primeiraNotaStr)
segundaNotaStr = input("Digite a segunda nota: ")
segundaNota = int(segundaNotaStr)
terceiraNotaStr = input("Digite a terceira nota: ")
terceiraNota = int(terceiraNotaStr)
quartaNotaStr = input("Digite a quarta nota: ")
quartaNota = int(quartaNotaStr)

media = (primeiraNota + segundaNota + terceiraNota + quartaNota) / 4
print("A média aritmética é", media)
